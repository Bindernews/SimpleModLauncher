package com.github.vortexellauncher.exceptions;

public class DuplicateNameException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -2409802751274708792L;

	public DuplicateNameException() {
	}

	public DuplicateNameException(String arg0) {
		super(arg0);
	}

	public DuplicateNameException(Throwable arg0) {
		super(arg0);
	}

	public DuplicateNameException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
